<?php
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// Set Post Data
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

$Email = $_POST['email'];
$Name = $_POST['name'];
$Subject = $_POST['subject'] ?: 'Не заполнено!';
$Message = $_POST['message'] ?: 'Не заполнено!';

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////






////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// Data To Mail
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

$Subject = "Сообщение от: $Email";
$To = 'olegblud@gmail.com';
$Headers = "Content-type: text/html; charset=utf-8 \r\n
            From: Olegblud <olegblud@gmail.com>\r\n";
$Message = "
        <html>
            <head>
                <title>Сообщение от: $Email</title>
            </head>
            <body>
                <p>Email:       $Email</p>
                <p>Имя:         $Name</p>
                <p>Тема:        $Subject</p>
                <p>Сообщение:   $Message</p>
            </body>
        </html>
    ";

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////






////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// Send Email
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////


if ($Email && mail($To, $Subject, $Message, $Headers)) {
    echo '<div class="Registration-Success">Сообщение отправлено!</div>';
}
else {
    echo '<div class="Registration-Error">Ошибка!</div>';
}

